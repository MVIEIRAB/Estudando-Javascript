const calculator = {
    sum: (a, b) => { return a + b },
    sub: (a, b) => { return a - b },
    mult: (a, b) => { return a * b },
    div: (a, b) => { return a / b }
}

console.log(calculator.sum(4, 2))
console.log(calculator.sub(5, 2))
console.log(calculator.mult(10, 2))
console.log(calculator.div(10, 5))